# 산술, 비교, 대입, 논리
# 멤버쉽 연산자[in]
coffee = "americano"
print('a' in coffee) # True
print('ameri' in coffee) # True
text = """""" # 두 문장 이상의 글
hangle = """가나다라마바사아자차카타파하"""
