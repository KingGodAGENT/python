# operator
# 토큰(기호) : +, -, *, %, and, or, not, in, ...
# 연산자 : 타겟팅들을 가공해주는 토큰들

# 산술 연산자 : 게산 해주는 토큰들 [+, -, *, %, /, **]
# 대입 연산자 : 변수에 대입을 해주는 토큰들 [=, +=, -=, ...]
# 논리 연산자 : 집합 연산을 해주는 토큰들 [and, or, not]
# 비교 연산자 : 우위를 연산해주는 토큰들 [==, !=, <, >, ...]
# 멤버쉽 연산자 : list, dict, set에 포함되는지 연산해주는 토큰
# 슬라이싱 연산자 : ex) hello[1:4] -> ell
# 인덱싱 연산자 : ex) apple[2] -> p