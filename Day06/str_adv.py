language = "python is great language."
# find
a = language.find("great")
print(a)

# replace
b = language.replace("python", "java")
print(b)

# upper, lower
c = language.upper()
print(c)

# split[찢다]
d = language.split(" ")
print(d)

# join
e = " ! ".join(["hello", "world"])
print(e)